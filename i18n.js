// Obtener idioma actual (guardado en localStorage o navegador)
function getCurrentLanguage() {
  return localStorage.getItem('language') || navigator.language.split('-')[0] || 'es';
}

// Guardar idioma seleccionado
function setLanguage(lang) {
  localStorage.setItem('language', lang);
  updatePageLanguage(lang);
}

// Traducir un texto
function translate(key) {
  const lang = getCurrentLanguage();
  return translations[lang]?.[key] || translations['es'][key] || key;
}

// Actualizar todos los elementos con data-i18n
function updatePageLanguage(lang) {
  document.documentElement.lang = lang;
  
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    const translation = translations[lang]?.[key] || translations['es'][key];
    
    if (translation) {
      if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
        element.placeholder = translation;
      } else {
        element.textContent = translation;
      }
    }
  });
  
  // Actualizar placeholders con data-i18n-placeholder
  document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
    const key = element.getAttribute('data-i18n-placeholder');
    const translation = translations[lang]?.[key] || translations['es'][key];
    
    if (translation) {
      element.placeholder = translation;
    }
  });
  
  // Actualizar el selector de idioma
  const langSelector = document.querySelector('[data-language-selector]');
  if (langSelector) {
    langSelector.value = lang;
  }
}

// Inicializar cuando se carga la página
document.addEventListener('DOMContentLoaded', function() {
  const currentLang = getCurrentLanguage();
  updatePageLanguage(currentLang);
  
  // Configurar evento del selector
  const langSelector = document.querySelector('[data-language-selector]');
  if (langSelector) {
    langSelector.addEventListener('change', function() {
      setLanguage(this.value);
    });
  }
});
